# Ler um temperatura em grau Celsius e apresentá-la em Fahrenheit
# A fórmula de conversão para Fahrenheit é F = (C * 9 / 5) + 32
# Sendo C= Temperatura em Celsius, F= Temperatura em Fahrenheit

C = int(input('Insira um valor em Celsius: '))
K = (C * 9 / 5) + 32
print('O valor convertido para Fahrenheit é de:',K)
